# 4. 泊松分布采样
# ============================================================================

r"""
## 4.1 泊松分布定义

泊松分布描述单位时间内随机事件发生的次数, PMF 为:

    P(X = k) = (λᵏ · e^(-λ)) / k! ,   k = 0, 1, 2, ...

其中 λ > 0 是参数, 同时也是均值和方差: E[X] = Var(X) = λ。

## 4.2 本实现采用的三档策略

不同 λ 范围下, 数值性质和效率需求差异很大。本实现根据 λ 大小
**自动选择最优算法**, 兼顾精度、稳定性与速度:

| λ 区间 | 算法 | 时间复杂度 | 特点 |
|--------|------|-----------|------|
| λ < 30 | Knuth 乘法算法 | O(λ) | 精确、简单、小 λ 最快 |
| 30 ≤ λ < 500 | 截断范围 CDF 递推 | O(√λ) ≈ 25~200 步 | 精确、数值绝对稳定 |
| λ ≥ 500 | 正态近似取整 | O(1) | 极快、偏度 < 0.045, 误差极小 |

---

## 4.3 第一档: λ < 30 — Knuth 乘法算法

### 原理

泊松过程的核心性质: **相邻事件的时间间隔服从指数分布 Exp(λ)**。

考虑 [0, 1] 时间区间内的事件数 N:
    N = max{ n : T₁ + T₂ + ... + Tₙ ≤ 1 }
其中 Tᵢ ~ Exp(λ) 独立同分布。

对指数分布使用逆变换 Tᵢ = -ln(Uᵢ)/λ, 代入得:
    ∏_{i=1}^{n} Uᵢ ≥ e^(-λ)

### 算法

    L = e^(-λ)
    k = 0, p = 1
    循环:
        k += 1
        p *= U   (U ~ Uniform(0,1), 独立)
        若 p < L: 返回 k-1

时间复杂度: O(λ) (期望循环次数 = λ + 1)

λ = 20 时平均 21 次乘法, 非常快; 但 λ = 1000 时需要 1001 次, 此时应切换。

---

## 4.4 第二档: 30 ≤ λ < 500 — 截断范围 CDF 递推法

### 设计思路

当 λ 增大, 泊松分布越来越集中在均值附近。根据切比雪夫不等式:
    P(|X - λ| ≥ kσ) ≤ 1/k²

取 k = 7, 则 P(|X - λ| ≥ 7σ) ≤ 1/49 ≈ 2%。实际上, 对于泊松这种近似
正态的分布, 7σ 以外的概率小于 10⁻¹¹, 几乎可以忽略。

因此只需在 [mode - 7σ, mode + 7σ] 范围内累积 CDF 即可。

### 递推关系

泊松 PMF 有简洁的递推式, 可避免重复计算阶乘和指数:

    P(k+1) = P(k) × λ / (k+1)
    P(k-1) = P(k) × k / λ

### 完整算法

    u ~ Uniform(0, 1)
    σ = √λ
    mode = ⌊λ⌋
    start_k = max(0, mode - ⌊7σ⌋ - 1)
    end_k   = mode + ⌊7σ⌋ + 10

    # 用 lgamma 精确计算起点概率
    log_P = start_k × ln(λ) - λ - ln(Γ(start_k + 1))
    P = exp(log_P)
    CDF = 0

    for k = start_k to end_k:
        CDF += P
        if u < CDF: return k
        P ×= λ / (k+1)

    # 若 u 落在极小概率尾部 (极罕见), 退化到从 0 开始完整累积

步数 = end_k - start_k ≈ 14σ = 14√λ:
- λ = 30:  ≈ 77 步
- λ = 100: ≈ 140 步
- λ = 499: ≈ 313 步

**关键优势**: 无拒绝采样, 无死循环, 数值 100% 稳定, 精度精确。

---

## 4.5 第三档: λ ≥ 500 — 正态近似取整

### 原理: 中心极限定理 (CLT)

当 λ → ∞, 标准化的泊松变量依分布收敛于标准正态:

    (X - λ) / √λ  →  N(0, 1)

近似误差由偏度衡量: 泊松分布的偏度 = 1/√λ

| λ | 偏度 | 最大误差量级 |
|----|------|-------------|
| 30 | 0.18 | 尚可但不推荐 |
| 100 | 0.10 | 较明显 |
| 500 | 0.045 | 很小 |
| 1000 | 0.032 | 极小 |

λ ≥ 500 时偏度 < 0.045, 对大多数实际应用足够精确。

### 算法

    Z ~ N(0, 1)       (通过 Box-Muller)
    X = λ + √λ × Z
    k = ⌊X + 0.5⌋     (四舍五入)
    return max(0, k)

时间复杂度: O(1), 与 λ 无关, 极快。

---

## 4.6 三种算法的横向对比

| 维度 | Knuth (λ<30) | 截断递推 (30≤λ<500) | 正态近似 (λ≥500) |
|------|-------------|---------------------|-----------------|
| 精度 | 精确 | 精确 | 近似 (极高精度) |
| 数值稳定性 | 好 | **最好** (无拒绝) | 好 |
| λ=20 速度 | 最快 | 慢 | 快 |
| λ=100 速度 | 慢 (100 步) | 快 (140 步) | 最快 |
| λ=1000 速度 | 极慢 (1000 步) | 快 (300 步) | **最快** |
| 是否会死循环 | 不会 | **绝对不会** | 不会 |

## 4.7 代码实现

泊松采样的完整实现见 [sample_poisson](file:///d:/trae-bz/TraeProjects/50039/sampling_engine.py#L223-L334) 函数,
根据 λ 大小自动在三档算法间切换。
"""

