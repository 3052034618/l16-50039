# ============================================================================
# 1. 逆变换采样 (Inverse Transform Sampling)
# ============================================================================

r"""
## 1.1 通用思路

逆变换采样是最基础、最通用的非均匀随机采样方法。其数学基础是以下定理:

**定理**: 设随机变量 X 的累积分布函数 (CDF) 为 F(x) = P(X ≤ x),
且 F 严格单调递增 (因此可逆)。令 U ~ Uniform(0, 1) 为标准均匀随机变量,
则随机变量 Y = F⁻¹(U) 服从与 X 相同的分布。

**证明**:
    P(Y ≤ x) = P(F⁻¹(U) ≤ x)
             = P(U ≤ F(x))       (F 单调递增, 不等式方向不变)
             = F(x)               (U ~ Uniform(0,1), 其 CDF 就是恒等函数)

因此 Y 的 CDF 也是 F, 即 Y 与 X 同分布。∎

**算法流程**:
    输入: 目标分布的逆 CDF 函数 F⁻¹
    1. 生成 u ~ Uniform(0, 1)
    2. 返回 x = F⁻¹(u)

## 1.2 对逆 CDF 的依赖

逆变换采样的适用性完全取决于 **CDF 是否可逆**:

### 需要满足的条件
1. **严格单调递增**: F(x₁) < F(x₂) 当且仅当 x₁ < x₂
   - 保证每个概率值 u 对应唯一的分位数 x
2. **连续**: 不存在跳跃间断点
   - 离散分布的 CDF 是阶梯函数, 但可以用 "广义逆" 推广
3. **解析式可求**: F⁻¹ 有闭合形式表达式 (否则需要数值求逆)

### 逆变换法的典型适用分布
| 分布 | CDF F(x) | 逆 CDF F⁻¹(u) |
|------|---------|---------------|
| 均匀 U(a,b) | (x-a)/(b-a) | a + u(b-a) |
| 指数 Exp(λ) | 1 - e^(-λx) | -ln(1-u)/λ ≈ -ln(u)/λ |
| 柯西 Cauchy | 0.5 + arctan(x)/π | tan(π(u - 0.5)) |
| 拉普拉斯 Laplace | (分段) | 符号函数分段形式 |

### 逆变换法的典型 **不适用** 分布
- **正态分布**: CDF 没有初等闭式表达 (Φ(x) 是误差函数 erf 的变形)
- **Gamma 分布 (形状参数非整数)**: CDF 涉及正则化不完全 Gamma 函数
- **Beta 分布**: CDF 涉及正则化不完全 Beta 函数
- **t 分布、F 分布**: 同样无初等闭式逆 CDF

对于这些分布, 我们需要使用其他方法:
- **Box-Muller**: 专门针对正态分布
- **拒绝采样 (Rejection Sampling)**: 用一个易采样的包络分布
- **组合法 (Composition)**: 把目标分布分解成若干简单分布的混合
- **变换法**: 利用已知分布的函数变换关系

## 1.3 推广: 离散分布的逆变换

对于离散分布, CDF 是右连续的阶梯函数, 此时使用 **广义逆**:

    F⁻¹(u) = min{ x : F(x) ≥ u }

算法流程:
    给定概率 p₀, p₁, ..., pₙ₋₁ (∑pᵢ = 1)
    1. 生成 u ~ Uniform(0, 1)
    2. 找到最小的 k 使得 ∑_{i=0}^{k} pᵢ ≥ u
    3. 返回 k

这就是离散分布的 "累积分布查找" 方法, 时间复杂度 O(n)。
**别名方法** (见下文) 可以将其优化到 O(1)。
"""

# ============================================================================
# 2. Box-Muller 变换
# ============================================================================

r"""
## 2.1 问题背景

标准正态分布 N(0, 1) 的概率密度函数为:

    φ(x) = (1/√(2π)) · e^(-x²/2)

其 CDF Φ(x) = ∫_{-∞}^{x} φ(t)dt 没有初等闭式表达, 因此无法直接使用逆变换法。

历史上曾用近似公式 (如 Abramowitz & Stegun 中的有理近似) 计算 Φ⁻¹,
但 Box-Muller 变换提供了一种精确且高效的方法。

## 2.2 变换推导

**核心思想**: 利用二维极坐标变换的雅可比行列式。

考虑两个独立标准正态变量 (X, Y) 的联合分布:

    f(x, y) = φ(x) · φ(y) = (1/2π) · e^(-(x²+y²)/2)

这是一个 **旋转对称** 的二维分布, 在极坐标下 (R, θ) 有特殊结构:

令:
    R = √(X² + Y²)      (径向距离)
    θ = arctan2(Y, X)   (极角)

则变换的雅可比行列式为 |J| = R, 因此极坐标下的联合 PDF 为:

    f(R, θ) = f(x, y) · |J| = (1/2π) · e^(-R²/2) · R

这可以分解成两个独立分布的乘积:

    f(R, θ) = f_R(R) · f_θ(θ)

其中:
    f_θ(θ) = 1/(2π),   θ ∈ [0, 2π)      (均匀分布!)
    f_R(R) = R · e^(-R²/2),  R ≥ 0      (瑞利分布)

## 2.3 逆变换求解 R 和 θ

### 极角 θ
θ ~ Uniform(0, 2π), 直接用逆变换:

    θ = 2π · U₂,    U₂ ~ Uniform(0, 1)

### 径向距离 R
瑞利分布的 CDF:
    F_R(r) = P(R ≤ r) = ∫₀ʳ t·e^(-t²/2) dt = 1 - e^(-r²/2)

求逆:
    u = 1 - e^(-r²/2)
    e^(-r²/2) = 1 - u
    r = √(-2 · ln(1 - u)) ≈ √(-2 · ln(u))   (因为 1-U 与 U 同分布)

## 2.4 完整算法 (Box-Muller 基本形式)

    输入: 两个独立的 U₁, U₂ ~ Uniform(0, 1)

    R  = √(-2 · ln(U₁))
    θ  = 2π · U₂

    Z₁ = R · cos(θ)
    Z₂ = R · sin(θ)

    输出: Z₁, Z₂ 是两个独立的 N(0, 1) 样本

**关键性质**: 一次变换同时产出两个独立正态样本, 这是该方法高效的关键。

## 2.5 数值优化: 极坐标形式 (Marsaglia Polar Method)

基本形式需要计算 cos 和 sin, 可以通过"拒绝采样 + 单位圆"的技巧避免:

    循环:
        生成 V₁, V₂ ~ Uniform(-1, 1)
        S = V₁² + V₂²
    直到 S < 1 且 S ≠ 0

    令因子 = √(-2 · ln(S) / S)
    Z₁ = V₁ · 因子
    Z₂ = V₂ · 因子

这避免了三角函数计算, 但有约 1 - π/4 ≈ 21% 的拒绝率。
在现代 CPU 上 cos/sin 很快, 两种形式的性能差距不大。

## 2.6 实现中的缓存技巧

由于每次调用生成两个样本, 但用户通常每次只需一个, 可以缓存第二个样本:

    def sample_normal():
        if 缓存非空:
            return 弹出缓存
        计算 Z1, Z2
        缓存 Z2
        return Z1

这在 [sampling_engine.py](file:///d:/trae-bz/TraeProjects/50039/sampling_engine.py)
的 sample_normal_boxmuller() 函数中实现。
"""

# ============================================================================
# 3. 别名方法 (Walker's Alias Method / Vose's Alias Method)
# ============================================================================

r"""
## 3.1 问题背景

给定 n 个离散结果 {0, 1, ..., n-1}, 对应概率 p₀, p₁, ..., pₙ₋₁,
需要高效地按照这个分布采样。

朴素方法 (累积分布查找):
    - 预计算前缀和数组 CDF[k] = ∑_{i=0}^{k} pᵢ
    - 生成 u ~ Uniform(0,1), 线性/二分查找最小的 k 使得 CDF[k] ≥ u
    - 时间复杂度: O(n) 或 O(log n) 每次采样

别名方法可以做到:
    - 预处理: O(n) 时间
    - 每次采样: O(1) 时间 (两次查表 + 一次比较)

## 3.2 几何直觉

想象有 n 个"槽位", 每个槽位的面积恰好为 1 (总面积 = n)。
将每个概率 pᵢ 乘以 n, 得到 qᵢ = n · pᵢ (总面积 ∑qᵢ = n, 保持不变)。

每个 qᵢ 代表第 i 个结果分配到的"颜料量":
- 如果 qᵢ < 1: 颜料不足以填满一个槽位 (欠载, underfull)
- 如果 qᵢ ≥ 1: 颜料可以填满至少一个槽位 (过载, overfull)

**关键观察**: 我们可以将"过载"的颜料倒入"欠载"的槽位, 使得每个槽位
最终恰好被填满 (面积 = 1)。这样每个槽位最多包含两种颜料:
- "本尊": 槽位原本对应的颜料 (占 prob[i] 比例)
- "别名": 从过载槽位倒入的颜料 (占 1-prob[i] 比例)

## 3.3 算法步骤

### 预处理 (构建别名表, Vose O(n) 算法)

输入: 概率 p[0..n-1]

1.  令 scaled[i] = n * p[i]
2.  创建两个队列:
    - underfull: 存入所有 scaled[i] < 1 的索引
    - overfull:  存入所有 scaled[i] ≥ 1 的索引
3.  当两个队列都非空:
    a. 弹出 under = underfull.pop()
    b. 弹出 over  = overfull.pop()
    c. prob[under]  = scaled[under]    (槽位 under 保留本尊的比例)
    d. alias[under] = over             (槽位 under 不足部分用 over 填补)
    e. scaled[over] = (scaled[over] + scaled[under]) - 1  (更新 over 剩余量)
    f. 若 scaled[over] < 1: underfull.push(over)
       否则: overfull.push(over)
4.  处理剩余队列中的元素 (理论上 prob 都应为 1):
    prob[i] = 1, alias[i] = i

### 采样 (O(1) 时间)

1.  均匀随机选择槽位: i = randint(0, n-1)
2.  生成 u = Uniform(0, 1)
3.  若 u < prob[i]: 返回 i (选本尊)
    否则: 返回 alias[i] (选别名)

## 3.4 正确性论证

对于结果 k, 它被选中的概率 = P(选中本尊) + P(作为别名被选中)

P(选中本尊 k) = (1/n) × prob[k] = prob[k] / n

P(作为别名被选中) = ∑_{i ≠ k} P(选中槽位 i 且别名是 k)
                  = ∑_{i: alias[i]=k} (1/n) × (1 - prob[i])

将这些加起来:
    P(k) = prob[k]/n + ∑_{i: alias[i]=k} (1-prob[i])/n

在算法中, 我们将 scaled[over] 的 prob[under] 部分分配给了 under,
同时将剩余 (scaled[over] + scaled[under] - 1) 保留给了 over 继续参与分配。
数学归纳可证, 最终 P(k) = p[k]。∎

## 3.5 代码实现

别名方法的完整实现见 [AliasSampler](file:///d:/trae-bz/TraeProjects/50039/sampling_engine.py#L295-L405) 类。
"""

